void get_defect(int rs, int re, int cs, int ce) { // all starts and ends are inclusive
    if (rs == re && cs == ce) {
        if (image[rs][cs] == 0) printf("Defect at: (%d, %d)\n", rs, cs);
        return;
    }
    int rm = (rs + re) / 2;
    int cm = (cs + ce) / 2;
    get_defect(rs, rm, cs, cm);
    get_defect(rs, rm, cm + 1, ce);
    get_defect(rm + 1, re, cs, cm);
    get_defect(rm + 1, re, cm + 1, ce);
}

